// Posições - 0  1  2  3  4
var num =    [1, 3, 2, 5, 4];

console.log(`Vetor = ${num}`);
console.log(`Vetor na posição 3 = ${num[3]}`);
console.log(`Tamanho do vetor = ${num.length}`);
console.log(`Adicionando um número no vetor = adicionando ${num.push(6)} -> ${num}`);
console.log(`Colocando o vetor em ordem = ${num.sort()}`);
console.log(`Retirando um número no vetor = retirando ${num.pop()} -> ${num}`);